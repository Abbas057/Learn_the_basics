package java_basics;

public class TypeCasting {
	

	public static void main(String[] args) {
		
		
		int a= 10;
		// Wide casting
		double b = a;
		float c=25.6f; 
		// Narrow casting
		int d = (int)c;
		
		System.out.println("Int :"+a);
		System.out.println("Double :"+b);
		System.out.println("float :"+c);
		System.out.println("int :"+d);
		
		

	}

}

